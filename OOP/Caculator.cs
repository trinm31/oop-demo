namespace OOP
{
    internal class Caculator
    {
        public double Add(int a, int b)
        {
            return a + b;
        }
        
        public double Add(double a, double b)
        {
            return a + b;
        } 
        
        public double Add(double a, double b, double c)
        {
            return a + b + c;
        }  
    }
}